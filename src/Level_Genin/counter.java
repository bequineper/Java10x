public class counter {
    public static void main(String[] args) {

        System.out.println("Contador iniciando...");
        System.out.println("======================");
        for (int i = 0; i <= 100; i++) {
            System.out.println("Contador: " + i);
        }
        System.out.println("======================");
        System.out.println("Contador chegou a 100...");

    }
}
